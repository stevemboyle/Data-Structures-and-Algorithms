###############################################################################
# *****************************************************************************
# ARRAY (FOR MONKEY-PATCHING)
# *****************************************************************************
###############################################################################

class Array

  # ***************************************************************************
  # HASH
  # ***************************************************************************

  def hash
    each_with_index.inject(0) do |intermediate_hash, (el, i)|
      (el.hash + i.hash) ^ intermediate_hash
    end
  end
end

###############################################################################
# *****************************************************************************
# STRING (FOR MONKEY-PATCHING)
# *****************************************************************************
###############################################################################

class String

  # ***************************************************************************
  # HASH
  # ***************************************************************************

  def hash
    chars.map(&:ord).hash
  end
end

###############################################################################
# *****************************************************************************
# HASH (FOR MONKEY-PATCHING)
# *****************************************************************************
###############################################################################

class Hash

  # ***************************************************************************
  # HASH
  # ***************************************************************************

  def hash
    to_a.sort_by(&:hash).hash
  end
end
