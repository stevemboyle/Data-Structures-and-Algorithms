###############################################################################
# *****************************************************************************
# STATIC ARAY
# *****************************************************************************
###############################################################################

class StaticArray

  # ***************************************************************************
  # INITIALIZE
  # ***************************************************************************
  # Create an Array of set length where every Index is nil
  # ***************************************************************************

  def initialize(length)
    self.store = Array.new(length, nil)
  end

  # ***************************************************************************
  # GET
  # O(1) Time Complexity for Look-Up in a Static Array
  # ***************************************************************************
  # Return the Value of the Store at that Index
  # ***************************************************************************

  def [](index)
    return store[index]
  end

  # ***************************************************************************
  # SET
  # O(1) Time Complexity for Setting a Value in a Static Array
  # ***************************************************************************
  # Set the Value of the Store at that Index
  # ***************************************************************************

  def []=(index, value)
    store[index] = value
  end

  protected
  attr_accessor :store
end
